import math

# Constants
print("Value of pi:", math.pi)
print("Value of Euler's number:", math.e)

# Basic arithmetic functions
print("Square root of 16:", math.sqrt(16))
print("Ceiling of 3.4:", math.ceil(3.4))
print("Floor of 3.6:", math.floor(3.6))
print("Absolute value of -5:", math.fabs(-5))
print("Power of 2^3:", math.pow(2, 3))
print("Factorial of 5:", math.factorial(5))

# Trigonometric functions
print("Sine of 30 degrees:", math.sin(math.radians(30)))
print("Cosine of 60 degrees:", math.cos(math.radians(60)))
print("Tangent of 45 degrees:", math.tan(math.radians(45)))
print("Inverse sine of 0.5:", math.degrees(math.asin(0.5)))
print("Inverse cosine of 0.5:", math.degrees(math.acos(0.5)))
print("Inverse tangent of 1:", math.degrees(math.atan(1)))

# Hyperbolic functions
print("Hyperbolic sine of 0:", math.sinh(0))
print("Hyperbolic cosine of 0:", math.cosh(0))
print("Hyperbolic tangent of 0:", math.tanh(0))
print("Inverse hyperbolic sine of 1:", math.asinh(1))
print("Inverse hyperbolic cosine of 2:", math.acosh(2))
print("Inverse hyperbolic tangent of 0.5:", math.atanh(0.5))

# Logarithmic functions
print("Natural logarithm of 10:", math.log(10))
print("Base-2 logarithm of 8:", math.log2(8))
print("Base-10 logarithm of 100:", math.log10(100))
print("Exponential of 2:", math.exp(2))

# Angular conversion functions
print("Radians to degrees:", math.degrees(math.pi))
print("Degrees to radians:", math.radians(180))
